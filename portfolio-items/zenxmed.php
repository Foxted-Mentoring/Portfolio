    <ul>

        <li>
            <h3>The Company</h3>
            <p>Zenxmed is a medical-tech startup that is building a mobile-first, SaaS platform to enable physicians to make informed treatment decisions, using machine learning and data science to visually present personalized patient data.</p>
        </li>

        <li>
            <h3>My Role</h3>
            <p>Conducting lots of user research, numerous interviews and user testing with the primary users (physicians across North America). However, since I signed an NDA and the product's beta version has not yet been released, I cannot reveal much of my work. What I can show here is the UX research that I've conducted, as well as the more traditional design projects that I've worked on: branding, website, newsletter and social media.</p>
        </li>

        <li>
            <h3>Website</h3>
            <p>To rebuild the original SquareSpace website in Wordpress in order to be able to have full customization and blogging capabilities, while revamping the visual branding.</p>
            <p>We were under pressure to build this site as quickly as possible so that we could focus on other aspects of the business. The challenge in designing the home page was that the product has not yet been released, so it was difficult to build a website without revealing the product's key features.</p>
        </li>

        <li>
            <h4>Audience</h4>
            <p>1) Physicians and clinicians, 2) Investors, 3) Tech Talent</p>
        </li>

        <li>
            <h4>Home Page</h4>
            <p>The initial home page was very long, so we decided to break it up into three pages (Home, About, Contact), to increase faster load time of the Home page, since it's critical that people do not get discouraged when landing on this initial page.</p>
        </li>

        <li>
            <h3>Visual Design</h3>
        </li>

        <li>
            <h4>Branding</h4>
            <p>The logo's circular "zen" brush-mark is overused in the design world for any concept relating to "zen". We wanted to have something unique that represented the core essence of the company. I looked up the definition of "zen" is "enlightenment by the most direct possible means", according to <a href="http://www.dictionary.com/browse/zen?s=t" target="_blank">Dictionary.com</a>, and really wanted to incorporat"e the meaning of "light" and "illumination" in our logo, as that is exactly what our product does (provides physicians with information and "enlightenment", in a sense).</p>
            <p>We experimented with many different design concepts and variations, and the final output illustrates the concept of light in a dynamic way.</p>
        </li>


        <li>
            <h4>Colour Palette</h4>
            <p>Blues and greens pervade the medical industry, and are also so cliché, so we wanted to break out of this pattern. We went with the one colour that none of the other competitors were using &emdash; orange. However, we toned the orange down so that it wouldn't be a gawdy, blaring shade, but went with a subdued shade. The final effect gives a warm and inviting feel, which was important to us.</p>
        </li>

        <li>
            <h4>Font Tests</h4>
            <p>We wanted to use something that was friendly yet legible and professional. Shaun chose the Museo Rounded Sans family and the rest of us agreed to it.</p>
        </li>

        <li>
            <h3>User Research</h3>
            <h4>Qualitative Research</h4>
            <p>Before starting the product design, we conducted one-on-one interviews with over 15 physicians in various medical specialties across North America. We gleaned some key insights into the most popular existing tools used by physicians, as well as as their biggest pain points in their practice.</p>
            <h4>Quantitative Research</h4>
            <p>We also conducted an online survey with over 170 physicians across North America to learn about the tools they use, and presented our key findings in our first newsletter (see below).</p>
        </li>


        <li>
            <h4>User Testing</h4>
            <p>After the first version of the mobile product was created, we were glad to see the overwhelmingly positive responses by doctors, which was greatly encouraging.</p>
        </li>

        <li>
            <h3>Newsletter</h3>
            <p>We wanted to show the key information as infographics to make it more engaging for physicians to read. I had to decipher any patterns and, after approval, came up with different ways to illustrate the highlights of the survey results.</p>
        </li>

        <li>
            <h3>Social Media</h3>
            <p>This banner design was used both for the Facebook and LinkedIn company pages. I wanted to use the same visual branding (blue-orange gradient, showing part of the logo in the background to reinforce recognition of the logo, and photo of a young female physician smiling.</p>
        </li>

        <li>
            <h3>Results Achieved</h3>
            <p>From the time that I joined Zenxmed in March 2016 and started rebuilding the website in August, many strides have been made in the company, and as a result, to the website. 1) We received funding through the Canadian National Research Council (IRAP); 2) attracted 40+ physicians, clinicians medical students and residents across North America to the company; and 3) managed to attract some software veterans in Vancouver to join us, which is no small feat, given the shortage of top tech talent in the city. While admittedly certain parts of the home page could use more work (icons, typography and partner logo), overall, the website redesign has been a success. The website will soon be getting a makeover to the home page, but overall, the website has been extremely effective in generating leads and furthering the company.</p>
        </li>

