            <h3>Website & Competitor Audit</h3>
            <p>Next, I performed a website audit to determine the strengths and weaknesses of their existing site. I then compared it to their top 5 competitors and analyzed their website from a design, content and navigational perspective.</p>
            <p>It was determined that 4/5 out of their top competitors were not up to par in their website designs, giving them a clear advantage in their online presence.</p>
        </li>

